const assert = require('node:assert/strict')
const fs = require('node:fs')
const path = require('node:path')
const test = require('node:test')

const root = path.resolve(__dirname, '..')
const source = fs.readFileSync(path.join(root, 'scripts/app-window.js'), 'utf8')

test('app window runs as a desktop-style launcher instead of the old browser page', () => {
    assert.match(source, /--app=\$\{url\}/)
    assert.match(source, /APP_WINDOW_WIDTH = 1500/)
    assert.match(source, /APP_WINDOW_HEIGHT = 930/)
    assert.match(source, /--start-maximized/)
    assert.match(source, /windowsHide:\s*true/)
    assert.match(source, /assets', 'logo\.png'/)
    assert.match(source, /rel="icon"/)
    assert.match(source, /\/manifest\.json/)
    assert.match(source, /chromium\.executablePath/)
    assert.match(source, /resolveBundledChromium\(\)/)
    assert.match(source, /spawn\(process\.execPath,\s*\['\.\/dist\/index\.js',\s*'--ui-child'\]/)
    assert.match(source, /LICENSE_KEY: pendingLicenseKey/)
    assert.match(source, /POST' && req\.url === '\/api\/start'/)
    assert.match(source, /POST' && req\.url === '\/api\/stop'/)
    assert.match(source, /POST' && req\.url === '\/api\/close'/)
    assert.match(source, /POST' && req\.url === '\/api\/open-accounts'/)
    assert.match(source, /POST' && req\.url === '\/api\/open-discord'/)
    assert.match(source, /Rewards Bot/)
    assert.match(source, /Today's Progress/)
    assert.match(source, /Points Overview/)
    assert.match(source, /Active Account/)
    assert.match(source, /Recent Activity/)
    assert.match(source, /Quick Actions/)
    assert.match(source, /Core license/)
    assert.match(source, /Continue without Core/)
    assert.match(source, /Manage account/)
    assert.match(source, /Console/)
    assert.match(source, /Discord/)
    assert.match(source, /Run daily set now/)
    assert.match(source, /Stop/)
    assert.doesNotMatch(source, /License key or empty response/)
    assert.match(source, /height:\s*100vh/)
    assert.match(source, /dashboard-grid/)
    assert.match(source, /MSRB_APP_NO_OPEN/)
    assert.match(source, /MSRB_APP_NO_BOT/)
})
